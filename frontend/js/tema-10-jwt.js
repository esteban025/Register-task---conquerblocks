// === JWT ===

// que es?
// JWT significa JSON Web Token

// es un estándar abierto (RFC 7519) que define una forma compacta y autónoma de transmitir información segura entre partes como un objeto JSON

// esta información puede ser verificada y confiable porque está firmada digitalmente

// estructura
// un JWT típicamente consta de tres partes separadas por puntos (.), que son:
// 1. Header (Encabezado)
// 2. Payload (Carga útil)
// 3. Signature (Firma)
